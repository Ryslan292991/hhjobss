"""Два уровня отсева: жёсткие стоп-слова в названии и балльный антифрод.
Работает с нормализованной Vacancy, источник не важен."""
import re

import config
from models import Vacancy

# ---------------------------------------------------------------------------
# 1. Жёсткий стоп-лист. Совпадение в названии — вакансия выбрасывается сразу.
# ---------------------------------------------------------------------------
TITLE_STOPWORDS = [
    "колл-центр", "колл центр", "call-центр", "call центр", "call center", "callcenter",
    "контакт-центр", "контактный центр",
    "оператор call", "оператор колл", "оператор кол-",
    "оператор звонков", "оператор на звонк", "оператор телефон", "оператор линии",
    "холодные звонки", "холодный обзвон", "обзвон клиентов", "телемаркетинг",
    "специалист по звонкам", "продажи по телефону",
    "менеджер по подбору персонала", "подбор персонала", "рекрутер", "recruiter",
    "специалист по подбору", "hr-менеджер",
]


def is_hard_excluded(title: str) -> str | None:
    low = (title or "").lower()
    for kw in TITLE_STOPWORDS:
        if kw in low:
            return kw
    return None


# ---------------------------------------------------------------------------
# 2. Антифрод.
# ---------------------------------------------------------------------------
SCAM_TITLE = [
    "чат-менеджер", "чат менеджер", "администратор чатов", "оператор чатов",
    "менеджер по общению", "специалист по переписке", "оператор переписки",
    "менеджер по переписке", "модератор чата",
    "работа в telegram", "работа в телеграм", "работа в тг",
    "подработка на дому", "наборщик текста", "набор текста на дому",
    "заполнение анкет", "тайный покупатель", "курьер-кассир",
    "финансовый агент", "финансовый помощник", "дропшиппинг",
]

# (вес, регулярка по описанию, человекочитаемая причина)
SCAM_DESC = [
    (4, r"внести\s+(взнос|залог|предоплат|депозит)", "просят внести взнос или залог"),
    (4, r"обучение\s+платн", "платное обучение"),
    (4, r"оплатит[ье]\s+(курс|обучение|доступ)", "просят оплатить курс или доступ"),
    (3, r"ежедневн\w*\s+выплат", "обещают ежедневные выплаты"),
    (3, r"выплаты\s+кажд\w+\s+день", "обещают выплаты каждый день"),
    (3, r"без\s+вложений", "формулировка «без вложений»"),
    (3, r"не\s+сетевой\s+маркетинг", "оговорка «не сетевой маркетинг»"),
    (3, r"быстр\w+\s+заработ", "обещают быстрый заработок"),
    (3, r"нужен\s+только\s+(телефон|смартфон|интернет)", "«нужен только телефон»"),
    (3, r"крипто|бинарн\w+\s+опцион|букмекер|ставк\w+\s+на\s+спорт", "крипта, ставки или букмекеры"),
    (3, r"накрутк|отзывы\s+за\s+деньги|лайки\s+за\s+деньги", "накрутка активности или отзывов"),
    (3, r"18\s*\+|интимн|эскорт|для\s+девушек\s+приятной", "признаки интим-подработки"),
    (2, r"пиши(те)?\s+(мне\s+)?в\s+(телеграм|telegram|тг|whatsapp|ватсап)", "связь только через мессенджер"),
    (2, r"t\.me/", "ссылка на личный телеграм в описании"),
    (2, r"гражданство\s+не\s+важно", "«гражданство не важно»"),
    (2, r"возраст\s+не\s+важен", "«возраст не важен»"),
    (2, r"доход\s+от\s+\d{6,}", "обещают шестизначный доход"),
]
SCAM_DESC = [(w, re.compile(p, re.I), label) for w, p, label in SCAM_DESC]

TAG_RE = re.compile(r"<[^>]+>")


def strip_html(text: str) -> str:
    return TAG_RE.sub(" ", text or "")


def scam_score(v: Vacancy) -> tuple[int, list[str]]:
    score = 0
    reasons: list[str] = []

    title = (v.title or "").lower()
    for kw in SCAM_TITLE:
        if kw in title:
            score += 4
            reasons.append(f"название содержит «{kw}»")

    text = strip_html(v.description).lower()
    for weight, pattern, label in SCAM_DESC:
        if pattern.search(text):
            score += weight
            reasons.append(label)

    if not v.employer:
        score += 3
        reasons.append("не указан работодатель")
    if v.employer_verified is False:
        score += 3
        reasons.append("работодатель не верифицирован площадкой")
    if v.is_anonymous:
        score += 2
        reasons.append("анонимная вакансия")

    if v.currency == "RUR" and (v.salary_from or 0) >= 150_000:
        score += 3
        reasons.append(f"нереалистичная зарплата без опыта: от {v.salary_from:,} ₽".replace(",", " "))

    if len(text) < 300 and (v.salary_from or 0) >= 80_000:
        score += 2
        reasons.append("очень короткое описание при высокой зарплате")

    return score, reasons


class Verdict:
    KEEP = "keep"
    WARN = "warn"
    DROP = "drop"


def classify(v: Vacancy) -> tuple[str, list[str]]:
    stopword = is_hard_excluded(v.title)
    if stopword:
        return Verdict.DROP, [f"стоп-слово «{stopword}»"]

    score, reasons = scam_score(v)
    if score >= config.SCAM_DROP_THRESHOLD:
        return Verdict.DROP, reasons
    if score >= config.SCAM_WARN_THRESHOLD:
        return Verdict.WARN, reasons
    return Verdict.KEEP, reasons
